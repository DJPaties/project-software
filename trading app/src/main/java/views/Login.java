    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String user = usr.getText();
        String ps = pass.getText();
        if(!user.isEmpty() && !ps.isEmpty()){
        for(int i = 0;i<fa.readUsers().size();i++)
        {
            if(user.equals(fa.readUsers().get(i).getName())&& ps.equals(fa.readUsers().get(i).getPassword())){
                activeUser = fa.readUsers().get(i);
                userpage us = new userpage();
                us.setVisible(true);
                this.dispose();
                break;
            }else{
                JOptionPane.showMessageDialog(null, "Wrong Username or Password", "Can't Login",JOptionPane.WARNING_MESSAGE);
            }
        }}else{
            JOptionPane.showMessageDialog(null, "Enter Username or Password", "Can't Login",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

